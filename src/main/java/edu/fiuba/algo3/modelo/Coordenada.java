package edu.fiuba.algo3.modelo;

public class Coordenada {

    private int x;
    private int y;

    public Coordenada(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int devolverX() {
        return this.x;
    }

    public int devolverY() {
        return this.y;
    }

}
